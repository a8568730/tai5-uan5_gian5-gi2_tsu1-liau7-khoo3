from 臺灣言語工具.解析整理.拆文分析器 import 拆文分析器
from 臺灣言語工具.解析整理.物件譀鏡 import 物件譀鏡
from 臺灣言語工具.解析整理.轉物件音家私 import 轉物件音家私
from 臺灣言語工具.音標系統.閩南語.通用拼音音標 import 通用拼音音標

class 轉高先生的數位典藏文本:
	資料庫 = True
	def __init__(self):
		分析器 = 拆文分析器()
		家私 = 轉物件音家私()
		譀鏡 = 物件譀鏡()

		檔案 = open('/home/Ihc/研究計劃/意泉計劃/國閩翻譯/台語數位典藏文本.txt')

		字音集合=[]
		數字集合 = set()
		for 行 in 檔案:
			if 行.strip()=='':
				continue
			資料 = 行.strip().split('\t')
			if len(資料) == 2:
				try:
					型, 音 = 資料
					try:
						句物件 = 分析器.產生對齊句(型, 音)
					except:
						句物件 = 分析器.產生對齊句(音, 音)
					標準句 = 家私.轉做標準音標(通用拼音音標, 句物件)
					字音集合.append((譀鏡.看型(標準句), 譀鏡.看音(標準句)))
				except:
					print(型, 音)
					pass
			else:
				raise RuntimeError('一逝有問題！！' + 行)
		
		print(數字集合)
		print(len(字音集合))

if __name__ == '__main__':
	轉高先生的數位典藏文本()
